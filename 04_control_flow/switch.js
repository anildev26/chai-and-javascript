// Switch Case

let month = "mar"

switch (month){
    case 1:
        console.log("January")
        break;
    case "mar":
        console.log("Feb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;
    case 6:
        console.log("June");
        break;
    case 7:
        console.log("July");

    default:
        console.log("Entered Wrong Month");
        break;
}